<h1>RedakteurIn werden</h1>
<p>Sollten Sie Interesse daran haben, sich der FHG News-Redaktion anzuschließen können Sie dies folgendermaßen tun.</p>

<h2>Kontaktaufnahme</h2>
<p>Setzen Sie sich zunächst mit uns in Kontakt. Dies können Sie entweder persönlich bei einem unserer Redakteure machen oder auf unserer Webseite. Um dies zu machen befolgen Sie folgende Schritte.</p>
<ul>
  <li>Öffnen Sie DuisPaper.de auf dem Browser Ihrer Wahl.</li>
  <li>Klicken Sie links im Menü (ggf. auf das Symbol X klicken) auf den Punkt „Kontakt“.</li>
  <li>Geben Sie nun ihren Vor- und Nachnamen an.</li>
  <li>Geben Sie Ihre E-Mail-Adresse an, damit wir Ihnen eine Rückantwort auf Ihre Anfrage schicken können.</li>
  <li>In das Textfeld „Betreff“ geben Sie bitte am besten „Redaktionsbewerbung – [Vorname] [Nachname]“ ([Vorname] und [Nachname] durch Ihren Vor- und Nachnamen ersetzen) an.</li>
  <li>Im Textfeld „Nachricht“ können Sie nun einige Informationen über sich schreiben.</li>
</ul>
<p>Nach der Kontaktierung werden Sie eine Antwort als Mail erhalten, in der wir Ihnen nähere Informationen zum RedakteurIn werden geben.</p>
