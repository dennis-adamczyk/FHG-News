<h1>Fehler auf der Webseite gefunden</h1>
<p>Sollten Sie einen Fehler jeglicher Art auf unserer Webseite gefunden haben, wenden Sie bitte folgende Schritte an. Um den Fehler zu melden gibt es zwei Möglichkeiten „online“ und „offline“.</p>

<h2>Melden des Fehlers (online)</h2>
<p>Wenn Sie den Fehler online melden wollen, also auf der Internetseite, können Sie dies gerne machen, indem Sie folgende Schritte ausführen.</p>
<ol>
  <li>Öffnen Sie DuisPaper.de auf dem Browser Ihrer Wahl.</li>
  <li>Klicken Sie links im Menü (ggf. auf das Symbol <i class="material-icons">menu</i> klicken) auf den Punkt „Fehler melden“.</li>
  <li>Geben Sie nun ihren Vor- und Nachnamen an.</li>
  <li>Geben Sie Ihre E-Mail-Adresse an, auf der Sie erreichbar sind. (Sie werden nach absenden des Formulars eine Bestätigungsmail erhalten.)</li>
  <li>In das Textfeld „URL“ geben Sie bitte die Webseiten-Adresse der Seite, auf der Sie den Fehler gefunden haben, an.</li>
  <li>Beschrieben Sie den entdeckten Fehler so detailliert wie möglich im Textfeld „Beschreibung des Fehlers“.</li>
  <li>Nun können Sie optional ein Screenshot (Bildschirmaufnahme) im Feld „Datei anhängen“ anfügen.</li>
  <li>Klicken Sie unten rechts auf das Symbol <i class="material-icons">send</i> um den Fehler zu melden.</li>
</ol>

<h2>Melden des Fehlers (offline)</h2>
<p>Wenn Sie den Fehler offline melden wollen, also persönlich, können Sie dies gerne machen, indem Sie eine der folgenden Schritte ausführen.</p>
<ul>
  <li>Sie treffen uns (die Redaktion) an einem unserer Redaktionstreffen in der Schule (Treffen momentan: dienstags in der Mittagspause im Raum 011 des Hauptgebäudes).</li>
  <li>Setzen Sie sich mit einer unserer Redakteure (zuständig für Webseite: Dennis Adamczyk [Chefautor]) in Kontakt und schildern Sie ihm den Fehler.</li>
</ul>
