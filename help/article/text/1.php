<h1>QR-Code-Scanner richtig benutzen</h1>
<p>Damit der QR-Code-Scanner auf der Internetseite <a href="https://duispaper.de/additional-content/">https://duispaper.de/additional-content/</a>
richtig benutzt wird, können Sie sich diesen Artikel zur richtigen Verwendung des Scanners verwenden.</p>

<h2>Richtige Verwendung</h2>
<p>Um auf die Seite für die Zusatzinhalte eines Artikels in der Schülerzeitung FHG News zu gelangen, führen Sie bitte folgende Schritte aus. Wenn unter einem Artikel kein QR-Code vorhanden ist, gibt es leider keine Zusatzinhalte zu diesem Artikel.</p>
<ol>
  <li>Öffnen Sie DuisPaper.de auf dem Browser Ihrer Wahl.</li>
  <li>Klicken Sie links im Menü (ggf. auf das Symbol <i class="material-icons">menu</i> klicken) auf den Punkt „Zusatzinhalte“.</li>
  <li>Halten Sie den QR-Code, den Sie scannen möchten bereit.</li>
  <li>Richten Sie ihre Kamera so aus, dass sich der QR-Code komplett in dem rot-markierten Bereich befindet.</li>
  <li>Nach wenigen Sekunden sollten Sie auf die richtige Seite weitergeleitet werden. (Wenn Sie die Internetseite auf einem Smartphone geöffnet haben, vibriert das Gerät meistens.)</li>
</ol>

<h2>Scann erfolgt nicht</h2>
<p>Wenn Sie die im Artikel „Richtige Anwendung“ aufgelisteten Schritte angewandt haben und Sie trotzdem nicht auf eine Unterseite weitergeleitet werden, können Sie folgendes machen.</p>
<ul>
  <li>Säubern Sie den QR-Code so, dass sich kein Staub, Tinte oder andere Störfaktoren auf dem Papier befinden.</li>
  <li>Variieren Sie mit der Entfernung der Kamera zum Blatt, damit sich der QR-Code richtig fokussieren kann.</li>
  <li>Laden Sie die Seite neu oder rufen Sie sie erneut auf.</li>
</ul>
<p>Sollte keiner dieser Schritte zum Ziel geführt haben, können Sie den Fehler auf der Unterseite „Fehler melden“ oder auf der Unterseite „Kontakt“ melden. Schildern Sie dazu Ihre Vorgehensweise so detailliert wie möglich.</p>
